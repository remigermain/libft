#include "libft.h"

void	ft_putunbr_fd(unsigned int n, int fd)
{
	if (n > 4294967295)
		n = (4294967295 - n);
	if (n > 9)
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putnbr_fd((n % 10), fd);
	}
	else
		ft_putchar_fd((n + '0'), fd);
}
